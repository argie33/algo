#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Get values from environment variables or sensible defaults
const isDev = process.env.NODE_ENV !== "production" && !process.argv.includes("--production");
const apiUrl = isDev
  ? (process.env.VITE_API_URL || "http://localhost:3001")
  : (process.env.API_URL || process.env.VITE_API_URL || "http://localhost:3001");

const environment = process.env.ENVIRONMENT || "development";

// Cognito values must come from env vars, never hardcoded
const userPoolId = isDev
  ? (process.env.VITE_USER_POOL_ID || 'us-east-1_DUMMY')
  : (process.env.USER_POOL_ID || 'us-east-1_XJpLb9SKX');

const userPoolClientId = isDev
  ? (process.env.VITE_USER_POOL_CLIENT_ID || 'dummy-client-id')
  : (process.env.USER_POOL_CLIENT_ID || '6smb0vrcidd9kvhju2kn2a3qrl');

const userPoolDomain = isDev
  ? (process.env.VITE_USER_POOL_DOMAIN || 'dummy-domain')
  : (process.env.USER_POOL_DOMAIN || 'stocks-trading-dev-626216981288.auth.626216981288.amazoncognito.com');

// Create environment configuration
const devConfig = {
  API_URL: apiUrl,
  USER_POOL_ID: userPoolId,
  USER_POOL_CLIENT_ID: userPoolClientId,
  USER_POOL_DOMAIN: userPoolDomain,
  BUILD_TIME: new Date().toISOString(),
  VERSION: "1.0.0-dev",
  ENVIRONMENT: environment,
};

// Update the runtime config for development
const configPath = path.join(__dirname, "..", "public", "config.js");
const configContent = `// Runtime configuration - ${environment.charAt(0).toUpperCase() + environment.slice(1)} Environment\n// This file is auto-generated by setup-dev.js\nwindow.__CONFIG__ = ${JSON.stringify(devConfig, null, 2)};\n`;

fs.writeFileSync(configPath, configContent);

// Create .env file for Vite
const envPath = path.join(__dirname, "..", ".env");
const envContent = `# ${environment.charAt(0).toUpperCase() + environment.slice(1)} Environment Configuration
# Auto-generated by setup-dev.js

# API Configuration
VITE_API_URL=${apiUrl}
VITE_SERVERLESS=false
VITE_ENVIRONMENT=${environment}

# Development Mode - use real API, not mock data
VITE_FORCE_DEV_AUTH=true
VITE_ENABLE_MOCK_DATA=false

# Build Configuration
VITE_BUILD_TIME=${new Date().toISOString()}
VITE_VERSION=1.0.0-dev

# AWS Cognito Configuration
VITE_COGNITO_USER_POOL_ID=${userPoolId}
VITE_COGNITO_CLIENT_ID=${userPoolClientId}
VITE_AWS_REGION=us-east-1
VITE_COGNITO_DOMAIN=${userPoolDomain}
VITE_COGNITO_REDIRECT_SIGN_IN=http://localhost:5173
VITE_COGNITO_REDIRECT_SIGN_OUT=http://localhost:5173

# Feature Flags
VITE_ENABLE_DEBUG=true
`;

fs.writeFileSync(envPath, envContent);

console.log(
  `🎉 ${environment.charAt(0).toUpperCase() + environment.slice(1)} environment configured!`
  +  (isDev ? ` (API: ${apiUrl}, Mock Data: OFF)` : "")
);
