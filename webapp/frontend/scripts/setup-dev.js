#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Get values from environment variables or sensible defaults
const isDev = process.env.NODE_ENV !== "production" && !process.argv.includes("--production");
const apiUrl = isDev
  ? (process.env.VITE_API_URL || "")  // empty = use Vite proxy (set VITE_PROXY_TARGET to route to AWS)
  : (process.env.API_URL || process.env.VITE_API_URL || "");

const environment = process.env.ENVIRONMENT || "development";

// Cognito values MUST come from environment variables (set by CI/CD or manually)
// NEVER hardcode production credentials
const userPoolId = isDev
  ? (process.env.VITE_COGNITO_USER_POOL_ID || 'us-east-1_DUMMY')
  : (process.env.VITE_COGNITO_USER_POOL_ID || '');

const userPoolClientId = isDev
  ? (process.env.VITE_COGNITO_CLIENT_ID || 'dummy-client-id')
  : (process.env.VITE_COGNITO_CLIENT_ID || '');

const userPoolDomain = isDev
  ? (process.env.VITE_COGNITO_DOMAIN || 'dummy-domain')
  : (process.env.VITE_COGNITO_DOMAIN || '');

// Create environment configuration
const devConfig = {
  API_URL: apiUrl,
  USER_POOL_ID: userPoolId,
  USER_POOL_CLIENT_ID: userPoolClientId,
  USER_POOL_DOMAIN: userPoolDomain,
  BUILD_TIME: new Date().toISOString(),
  VERSION: "1.0.0-dev",
  ENVIRONMENT: environment,
};

// Update the runtime config for development
const configPath = path.join(__dirname, "..", "public", "config.js");
const configContent = `// Runtime configuration - ${environment.charAt(0).toUpperCase() + environment.slice(1)} Environment\n// This file is auto-generated by setup-dev.js\nwindow.__CONFIG__ = ${JSON.stringify(devConfig, null, 2)};\n`;

fs.writeFileSync(configPath, configContent);

// REMOVED: .env files violate CLAUDE.md security policy (NO .env files. Ever.)
// Vite and frontend use window.__CONFIG__ from config.js instead
// All credentials come from environment variables during build time, never stored in .env
// See: https://claude.md#security-baseline

console.log(
  `🎉 ${environment.charAt(0).toUpperCase() + environment.slice(1)} environment configured!`
  +  (isDev ? ` (API: ${apiUrl}, Mock Data: OFF)` : "")
);
